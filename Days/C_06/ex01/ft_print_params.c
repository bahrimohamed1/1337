#include<unistd.h>

int	main(int arc, char *arv[])
{
	int	i;
	int	j;

	i = 1;
	if (arc > 1)
	{
		while (arv[i])
		{
			j = 0;
			while (arv[i][j])
			{
				write (1, &arv[i][j], 1);
				j++;
			}
			i++;
			write (1, "\n", 1);
		}
	}
}
