#include<unistd.h>

int	main(int arc, char *arv[])
{
	int	i;
	int	j;

	i = arc - 1;
	if (arc > 1)
	{
		while (arv[i] && i >= 1)
		{
			j = 0;
			while (arv[i][j])
			{
				write(1, &arv[i][j], 1);
				j++;
			}
			write(1, "\n", 1);
			i--;
		}
	}
}
