int	ft_atoi(const char *nptr)
{
	int			i;
	const char	*str;
	int			s;
	int			r;

	str = nptr;
	i = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	s = 1;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			s = -1;
		i++;
	}
	r = 0;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		r = ((str[i] - 48) + r * 10);
		i++;
	}
	return (r * s);
}
